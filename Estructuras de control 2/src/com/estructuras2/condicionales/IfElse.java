package com.estructuras2.condicionales;

public class IfElse {
    public static void main(String[] args) {

        int edad = 16;
        int edad2 = 19;

        if (edad >= 18) {
            System.out.println("Es mayor de edad");
        } else {
            System.out.println("Es menor de edad");
        }

        if (edad2 >= 18) {
            System.out.println("Es mayor de edad");
        } else {
            System.out.println("Es menor de edad");
        }

    }
}
